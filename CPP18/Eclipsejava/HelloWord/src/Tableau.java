
public class Tableau {
 
	public static void main(String[] args) {
		
	int tab[][]= {{5,3,6,8,3}, {5,6,8,10,3} , {58,69,8,15,3}  
	
	};
	
	for(int i=0; i< tab.length; i++) {
		
		for(int j=0; j< tab[i].length;j++) {
			
			System.out.printf(tab[i][j] +" ");
		}
		
		
		System.out.println();
		
	}
	
	}
}
